	public void user_enter_the_and(String userId, String passId) {
		WebElement email = driver.findElement(By.id("email"));
		email.sendKeys(userId);
		WebElement pass = driver.findElement(By.id("pass"));
		pass.sendKeys(passId);
		
	}

	@Then("user sees the result based on {string}")
	public void user_sees_the_result_based_on(String Type) {
	   if(Type.equals("positive")) {
		   Assert.assertEquals("Log Out button not Available", driver.findElement(By.id("Log out")));
	   }else {
		   WebElement Errormsg = driver.findElement(By.xpath("//*[text()=\"The password that you've entered is incorrect. \"]"));
		   Assert.assertTrue("Error", Errormsg.isDisplayed());
	   }
	}


